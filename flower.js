 console.log('subtraction =',y-7);
 console.log('Multiplication =',x*7)
 console.log('division=',y/x)
 console.log('remainder=',x%y)
 console.log('increment x++ =',x++)
 console.log('decrement y-- =',y--)
 //Relation Operators
 console.log('greater than=',x>7)
 console.log('lesser than =',x<7)
 console.log('equal to=',x==y)
 console.log('Not equal=',x!==y)
 console.log('greater than or equal to =',x>=y)
 console.log('lesser than or equal to =',x<=y)
 // Logical operators
 console.log('logical and=',x>8 && y>9)
